/*jshint node:true */

"use strict";

var fs    = require("fs"),
    path  = require("path"),
    shell = require("shelljs");

module.exports = function(config) {
    var pkg;
    
    for(pkg in config.package.dependencies) {
        if(pkg === "yui") {
            continue;
        }
        
        shell.cp("-R", path.join("./node_modules", pkg), "./temp/node_modules");
    }
};
